public class Sample5 {
  public static void main(String[] args) {
    System.out.println(5 == 5);
    System.out.println(5 == 7);
    System.out.println("-----------");

    System.out.println(5 != 5);
    System.out.println(5 != 7);
    System.out.println("-----------");

    System.out.println(5 > 7);
    System.out.println(7 > 5);
    System.out.println("-----------");

    System.out.println(5 < 7);
    System.out.println(7 < 5);
    System.out.println("-----------");

    System.out.println(5 >= 7);
    System.out.println(5 >= 5);
    System.out.println(7 >= 5);
    System.out.println("-----------");

    System.out.println(5 <= 7);
    System.out.println(5 <= 5);
    System.out.println(7 <= 5);
    System.out.println("-----------");

  }
}